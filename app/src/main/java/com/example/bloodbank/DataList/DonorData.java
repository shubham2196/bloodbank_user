package com.example.bloodbank.DataList;

public class DonorData {

    String username;
    String address;
    String city;
    String state;
    String country;
    String pinCode;
    String mobileNo;
    String bloodGroup;

    public String getUsername() {
        return username;
    }

    public String getAddress() {
        return address;
    }

    public String getCity() {
        return city;
    }

    public String getState() {
        return state;
    }

    public String getCountry() {
        return country;
    }

    public String getPinCode() {
        return pinCode;
    }

    public String getMobileNo() {
        return mobileNo;
    }

    public String getBloodGroup() {
        return bloodGroup;
    }
}
